export function useLang() {
  const [lang, setLangState] = useState(() => localStorage.getItem('chincha-lang') || 'th');
  const setLang = (l) => {
    setLangState(l);
    localStorage.setItem('chincha-lang', l);
  };
  const t = (key) => T[lang]?.[key] ?? T.th?.[key] ?? key;
  return { lang, setLang, t };
}
